#!/usr/bin/env node
/**
 * F150: Generate guide catalog from YAML source files.
 *
 * Reads guides/registry.yaml + guides/flows/*.yaml and produces:
 *   packages/web/src/lib/guide-catalog.gen.ts
 *
 * Run: node scripts/gen-guide-catalog.mjs
 */
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const GUIDES_DIR = resolve(ROOT, 'guides');
const REGISTRY_PATH = resolve(GUIDES_DIR, 'registry.yaml');
const OUTPUT_PATH = resolve(ROOT, 'packages/web/src/lib/guide-catalog.gen.ts');

/** Map YAML step type+action to runtime expectedAction */
function mapExpectedAction(step) {
  if (step.type === 'information') return 'confirm';
  return step.action ?? 'click';
}

function loadRegistry() {
  if (!existsSync(REGISTRY_PATH)) {
    throw new Error(`Registry not found: ${REGISTRY_PATH}`);
  }
  const raw = readFileSync(REGISTRY_PATH, 'utf-8');
  const parsed = YAML.parse(raw);
  if (!parsed?.guides || !Array.isArray(parsed.guides)) {
    throw new Error('Registry must contain a "guides" array');
  }
  return parsed.guides;
}

function loadFlow(flowFile) {
  const flowPath = resolve(GUIDES_DIR, flowFile);
  if (!existsSync(flowPath)) {
    throw new Error(`Flow file not found: ${flowPath}`);
  }
  const raw = readFileSync(flowPath, 'utf-8');
  return YAML.parse(raw);
}

function validateFlow(flow) {
  const errors = [];
  if (!flow.id) errors.push('Missing flow.id');
  if (!flow.name) errors.push('Missing flow.name');
  if (!flow.steps || !Array.isArray(flow.steps)) {
    errors.push('Missing or invalid flow.steps');
    return errors;
  }
  const stepIds = new Set();
  for (const step of flow.steps) {
    if (!step.id) errors.push('Step missing id');
    if (stepIds.has(step.id)) errors.push(`Duplicate step id: ${step.id}`);
    stepIds.add(step.id);
    if (!step.type) errors.push(`Step ${step.id}: missing type`);
    if (step.type === 'console_action' && !step.target) {
      errors.push(`Step ${step.id}: console_action requires target`);
    }
    if (!step.instruction) errors.push(`Step ${step.id}: missing instruction`);
    if (!step.title) errors.push(`Step ${step.id}: missing title`);
  }
  // AC-S3: exit path — at least one step must allow skip/cancel
  const hasExitPath = flow.steps.some((s) => s.canSkip === true);
  if (!hasExitPath) errors.push('Flow has no exit path (no step with canSkip: true)');
  return errors;
}

function generateTS(registryEntries, flows) {
  const lines = [
    '/* eslint-disable */',
    '// AUTO-GENERATED by scripts/gen-guide-catalog.mjs — DO NOT EDIT',
    '// Source: guides/registry.yaml + guides/flows/*.yaml',
    '',
    "import type { GuideStep } from '@/stores/guideStore';",
    '',
    'export interface GuideFlow {',
    '  id: string;',
    '  name: string;',
    '  description: string;',
    '  steps: GuideStep[];',
    '}',
    '',
    'export interface GuideRegistryEntry {',
    '  id: string;',
    '  name: string;',
    '  description: string;',
    '  keywords: string[];',
    '  category: string;',
    '  priority: string;',
    '  crossSystem: boolean;',
    '  estimatedTime: string;',
    '}',
    '',
  ];

  // Emit registry
  lines.push('export const GUIDE_REGISTRY: GuideRegistryEntry[] = [');
  for (const entry of registryEntries) {
    lines.push('  {');
    lines.push(`    id: ${JSON.stringify(entry.id)},`);
    lines.push(`    name: ${JSON.stringify(entry.name)},`);
    lines.push(`    description: ${JSON.stringify(entry.description)},`);
    lines.push(`    keywords: ${JSON.stringify(entry.keywords)},`);
    lines.push(`    category: ${JSON.stringify(entry.category)},`);
    lines.push(`    priority: ${JSON.stringify(entry.priority)},`);
    lines.push(`    crossSystem: ${entry.cross_system ?? false},`);
    lines.push(`    estimatedTime: ${JSON.stringify(entry.estimated_time)},`);
    lines.push('  },');
  }
  lines.push('];');
  lines.push('');

  // Emit flows
  lines.push('export const GUIDE_FLOWS: Record<string, GuideFlow> = {');
  for (const flow of flows) {
    lines.push(`  ${JSON.stringify(flow.id)}: {`);
    lines.push(`    id: ${JSON.stringify(flow.id)},`);
    lines.push(`    name: ${JSON.stringify(flow.name)},`);
    lines.push(`    description: ${JSON.stringify(flow.description)},`);
    lines.push('    steps: [');
    for (const step of flow.steps) {
      lines.push('      {');
      lines.push(`        id: ${JSON.stringify(step.id)},`);
      if (step.target) {
        lines.push(`        targetGuideId: ${JSON.stringify(step.target)},`);
      }
      lines.push(`        title: ${JSON.stringify(step.title)},`);
      lines.push(`        instruction: ${JSON.stringify(step.instruction)},`);
      lines.push(`        expectedAction: ${JSON.stringify(mapExpectedAction(step))},`);
      if (step.canSkip != null) lines.push(`        canSkip: ${step.canSkip},`);
      if (step.timeoutSec != null) lines.push(`        timeoutSec: ${step.timeoutSec},`);
      lines.push('      },');
    }
    lines.push('    ],');
    lines.push('  },');
  }
  lines.push('};');
  lines.push('');

  return lines.join('\n');
}

// Main
const registryEntries = loadRegistry();
const flows = [];

for (const entry of registryEntries) {
  const flow = loadFlow(entry.flow_file);
  const errors = validateFlow(flow);
  if (errors.length > 0) {
    console.error(`\x1b[31m[F150] Flow ${entry.id} validation failed:\x1b[0m`);
    for (const e of errors) console.error(`  - ${e}`);
    process.exit(1);
  }
  flows.push(flow);
  console.log(`  \x1b[32m✓\x1b[0m ${entry.id} (${flow.steps.length} steps)`);
}

const output = generateTS(registryEntries, flows);
writeFileSync(OUTPUT_PATH, output, 'utf-8');
console.log(`\x1b[32m[F150] Guide catalog generated → ${OUTPUT_PATH}\x1b[0m`);
